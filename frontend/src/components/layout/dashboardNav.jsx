export function isNavItemActive(pathname, href) {
  if (href === '/invoices') {
    // Keep "Invoices" active on detail pages but not on /invoices/new
    return pathname === '/invoices' || /^\/invoices\/(?!new$).+/.test(pathname);
  }
  if (href === '/clients') {
    return pathname === '/clients' || pathname.startsWith('/clients/');
  }
  return pathname === href;
}

